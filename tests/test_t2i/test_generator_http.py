@pytest.mark.asyncio
@respx.mock
async def test_poll_until_ready(tmp_path: Path):
    respx.post("https://stub.test/submit").mock(
        return_value=httpx.Response(200, json={"id": "job-1"})
    )
    status_route = respx.get("https://stub.test/status/job-1")
    status_route.side_effect = [
        httpx.Response(200, json={"status": "processing"}),
        httpx.Response(
            200,
            json={
                "status": "completed",
                "output": {"url": "https://stub.test/img/job-1.png"},
            },
        ),
    ]
    respx.get("https://stub.test/img/job-1.png").mock(
        return_value=httpx.Response(200, content=_TINY_PNG)
    )

    ct = CostTracker(hard_cap_usd=10.0)
    gen = _StubPollGenerator(ct)
    async with gen:
        result = await gen.generate("p005", "poll test", tmp_path)

    assert result.status == GenerationStatus.SUCCESS
    assert status_route.call_count == 2


