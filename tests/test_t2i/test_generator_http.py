@pytest.mark.asyncio
@respx.mock
async def test_retry_on_429(tmp_path: Path):
    route = respx.post("https://stub.test/generate")
    route.side_effect = [
        httpx.Response(429, text="rate limited"),
        httpx.Response(200, content=_TINY_PNG),
    ]

    ct = CostTracker(hard_cap_usd=10.0)
    gen = _StubGenerator(ct)
    async with gen:
        result = await gen.generate("p001", "a cat", tmp_path)

    assert result.status == GenerationStatus.SUCCESS
    assert route.call_count == 2


