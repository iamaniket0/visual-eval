class _StubGenerator(BaseGenerator):
    """Minimal concrete generator for testing base class retry logic."""

    model_id = "_stub_test"
    provider = "test"

    def __init__(self, cost_tracker: CostTracker, api_url: str = "https://stub.test/generate"):
        config: dict[str, Any] = {
            "api_key_env": "_STUB_TEST_KEY",
            "cost_per_image": 0.01,
            "pattern": "sync",
        }
        super().__init__(config, cost_tracker, concurrency=1)
        self.api_key = "test-key"
        self.api_url = api_url

    async def _do_generate(self, prompt_text: str) -> tuple[bytes, dict]:
        r = await self.client.post(self.api_url, json={"prompt": prompt_text})
        r.raise_for_status()
        return r.content, {}


class _StubPollGenerator(BaseGenerator):
    """Minimal generator for testing async poll flow."""

    model_id = "_stub_poll"
    provider = "test"

    def __init__(self, cost_tracker: CostTracker):
        config: dict[str, Any] = {
            "api_key_env": "_STUB_POLL_KEY",
            "cost_per_image": 0.01,
            "pattern": "async_poll",
        }
        super().__init__(config, cost_tracker, concurrency=1)
        self.api_key = "test-key"

    async def _do_generate(self, prompt_text: str) -> tuple[bytes, dict]:
        r = await self.client.post("https://stub.test/submit", json={"prompt": prompt_text})
        r.raise_for_status()
        job_id = r.json()["id"]

        data = await self._poll_until_ready(
            f"https://stub.test/status/{job_id}",
            headers={"Authorization": "Bearer test-key"},
            poll_interval=0.01,
            max_wait=2.0,
            ready_check=lambda d: d.get("status") == "completed",
            failed_check=lambda d: d.get("error"),
        )
        img_url = data["output"]["url"]
        img_bytes = await self._download(img_url)
        return img_bytes, data


